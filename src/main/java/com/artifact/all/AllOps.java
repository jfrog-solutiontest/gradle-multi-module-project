package com.artifact.all;

public class AllOps {

    public static int sub(int a, int b) {
        return a - b;
    }

    public static int mul(int a, int b) {
        return a * b;
    }

    public static int divide(int a, int b) {
        return a / b;
    }

    public static int add(int a, int b) {
        return a + b;
    }
}
